# There are five main pre-processing functions below.  They should typically be
# run in the order in which they are described.  Note that old pre-processing 
# scripts for use with the LSD dealt extensively with punctuation and
# capitalization, but that is no longer the case here, since there are many
# packages for R that remove punctuation and capitalization.

# However, the first two functions here require that punctuation has NOT yet
# been removed, while the last two functions require that SPACES have been
# inserted around all punctuation.

# The third function, below, inserts spaces between text and punctuation.  It
# can be modified to include additional punctuation.

# There are also several optional pre-processing functions that will allow you
# to remove punctuation within some types of tokens that include punctuation,
# such as numbers, acronyms, and abbreviations.  This will prevent the function
# that adds space around punctuation from splitting those elements into multiple
# tokens.

# There is a final optional function that will mark out capitalized words (other
# than the first word in a sentence), so they will NOT be counted when the LSD 
# dictionaries are applied.  As per Young & Soroka (2012), proper nouns, by 
# definition, cannot have sentiment.  Therefore counting sentiment words that 
# coincide with individual's names, company names, etc., will overinflate 
# sentiment results.   The function to mark proper nouns will help address this 
# concern, to some extent.  Though note that it cannot distinguish between a 
# regulat word and a proper noun at the beginning of a sentence (as written,
# words at the beginning of sentences will not be marked off with an underscore,
# even if they are proper nouns).

#### WARNING ####
# Note that these functions make changes to the text - changes that will not
# typically be useful for content analyses other than the estimation of
# sentiment using the LSD.  If users are running several different analyses
# of text, it is likely important to keep one batch that is pre-processed
# using these functions, for LSD specifically; and another batch that is
# not pre-processed using these functions, for all other analyses.  To be
# clear: these are not general-purpose pre-processing scripts, they are
# designed only to improve the dictionary counts from the LSD2015 dictionary.


# The 5 major LSD pre-processing functions are as follows:

# LSDprep_contr: The function replaces a number of contractions that are
# relevant to properly identifying negations with the full negation.  For
# instance, "ain't" is replaced with "am not".  This will improve the
# identification of negations when running the LSD2015_NEG dictionary.  This
# function should be used BEFORE any changes are made to punctuation.

# LSDprep_dict_punct: This function addresses dictionary words that should not
# be included in dictionary counts, and that can be identified in relation to
# surrounding punctuation.  For example, "well" should not be counted as a
# positive word, when it is used at the beginning of a sentence, as so: " Well,
# ".  The function changes this usage to " xwell ".   This function should be
# used BEFORE any changes are made to punctuation.

# LSDprep_punctspace: This function inserts spaces around punctuation marks, to 
# ensure that all instances of phrases in the following two functions will work 
# correctly.  In lieu of this function, you may also run an alternative function
# that simply removes all punctuation from the texts.  However, keep in mind 
# that if you simply remove all the punctuation, you will not be able to use the
# Lexicoder functions that identify colocations within sentences.  Note: this 
# function will not distinguish between punctuation within numbers and other 
# punctuation.  The first two optional pre-processing functions, explained below, are
# intended to address some of these issues.

# LSDprep_negation: This function takes a wide range of phrases that indicate 
# negations, such as "not very" and "not always", and turns them all into just 
# "not", which improves the identification of negations when running the 
# LSD2015_NEG dictionary.  The R code depends on there being spaces around 
# words, therefore this function should be used either AFTER punctuation has
# been removed, OR AFTER spaces have been inserted around punctuation marks.

# LSDprep_dict: This function destroys variations of dictionary words that 
# should not be included in dictionary counts.  For instance, "well" should not 
# be counted as positive word when it appears in the phrase "may very well". 
# This function changes "may very well" to "may very xwell", which prevents 
# "well" from being counted when running the LSD2015 dictionary.  The R code 
# depends on there being spaces around words, therefore this function should be
# used either AFTER punctuation has been removed, OR AFTER spaces have been 
# inserted around punctuation marks.

## The three optional pre-processing functions are as follows:

# remove_punctuation_from_acronyms: This function identifies periods in between
# single capital letters, and removes the punctuation.  For example "U.S.A."
# becomes USA.  It does NOT alter lower case acronyms (i.e. "u.s.a.").  See
# function to "remove_punctuation_from_abbreviations" to remove periods from
# Upper-Case acronyms with spaces (i.e. "U. S. A.").

# remove_punctuation_from_abbreviations: This function removes periods from the
# end of common abbreviations.  For the most part it leaves the abbreviations
# untouched, with a few exceptions (such as replacing "No. 9" with "number 9").
# This function also removes periods after single capital letters, such as for
# middle names or space-separated acronyms.

# mark_proper_nouns: This function marks off words capitalized within the text 
# (but not capitalized words that follow sentence-ending punctuation, such as
# periods). It inserts an underscore after the first letter so that the words
# are not picked up when applying the LSD dictionary.

