mark_proper_nouns <- function(x) {
     require(stringr)
	x <- str_replace_all(x, pattern = "([A-Za-z0-9'] )([A-Z])", replacement = "\\1\\2_")
	x <- str_replace_all(x, pattern = "(\\()(| )([A-Z])", replacement = "\\1\\2\\3_")
	x <- str_replace_all(x, pattern = "(,)(| )([A-Z])", replacement = "\\1 \\3_")
	x <- str_replace_all(x, pattern = "([_] )([A-Z])", replacement = "\\1\\2_")
	# Note: the following line removes extraneous underscores, if a word is marked by an underscore more than once by previous lines
	x <- str_replace_all(x, pattern = "([A-Z]_)(____|___|__|_)", replacement = "\\1")
}




